// Funzione per mostrare la finestra di informazioni
function mostraFinestra() {
    var finestra = document.getElementById("finestra");
    finestra.style.display = "block";
}

// Funzione per chiudere la finestra di informazioni
function chiudiFinestra() {
    var finestra = document.getElementById("finestra");
    finestra.style.display = "none";
}
